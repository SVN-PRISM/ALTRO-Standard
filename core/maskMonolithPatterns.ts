/**
 * Реестровый монолит: `№` / U+2116 / `No.` / `No` + непробельный хвост до пробела/новой строки (`\S+`).
 */
export const REGISTRY_NUMBER_GREEDY_SOURCE = String.raw`(?:\u2116|№|No\.?)\s*\S+`;

/**
 * Роль Заказчик/Поставщик + разделитель (: / — / – / -) + опционально АО/ООО/… + имя в «» или "…".
 */
/** Разделитель роль↔тело: двоеточие или любой Unicode «dash» (\p{Pd}). */
export const ORGANIZATION_ROLE_QUOTED_SOURCE = String.raw`(?:^|(?<![\p{L}\p{N}_]))(?:Заказчик|Поставщик|заказчик|поставщик)\s*(?:\p{Pd}+|:)\s*(?:(?:АО|ООО|ЗАО|ПАО|ИП|ао|ооо|зао|пао|ип)\s+)?(?:"[^"]+"|«[^»]+»)(?![\p{L}\p{N}_])`;

/**
 * Дата словами (род. падеж): «15 января 2026» — один монолит до числовых/ISO-паттернов фазы 2.
 */
export const DATE_MONOLITH_SOURCE = String.raw`(?:^|(?<![\p{L}\p{N}_]))[0-9]{1,2}\s+(?:января|февраля|марта|апреля|мая|июня|июля|августа|сентября|октября|ноября|декабря)\s+[0-9]{4}(?!\d)`;

/**
 * ФИО: приоритет полной тройки «Фамилия Имя Отчество»; инициалы + фамилия; исключения заголовков договора.
 */
/** Не начинать «ФИО» с ролей/заголовков канцелярии (иначе «Подписант Иван Петров» съедается как тройка). */
const PERSON_ROLE_HEAD_BLACKLIST = String.raw`Дополнительное|Настоящее|Общие|Следующие|Прочие|Соглашение|Подписант|Заказчик|Исполнитель|Сторона|Договор|Представитель|Уполномоченный|Работник|Работодатель|Арендодатель|Арендатор`;

export const PERSON_FULL_NAME_GREEDY_SOURCE = String.raw`(?<![\p{L}\p{N}_])(?:\p{Lu}\.\s*\p{Lu}\.\s+\p{Lu}\p{L}+|\p{Lu}\p{L}+\s+\p{Lu}\.\s*\p{Lu}\.|(?!(?:${PERSON_ROLE_HEAD_BLACKLIST})\s)(?:\p{Lu}\p{L}+\s+){2}\p{Lu}\p{L}+|(?!(?:${PERSON_ROLE_HEAD_BLACKLIST})\s)(?:\p{Lu}\p{L}+\s+){1,2}\p{Lu}\p{L}+)(?![\p{L}\p{N}_])`;

/**
 * ИНН + опционально КПП в радиусе 50 символов (один монолит для Masker / SemanticFirewall).
 */
export const ORG_INN_KPP_MONOLITH_SOURCE = String.raw`(?:^|(?<![\p{L}\p{N}_]))(?:ИНН|инн)\s*:?\s*\d{10}(?:\d{2})?(?:[\s\S]{0,50}?(?:КПП|кпп)\s*:?\s*\d{9})?(?!\d)`;

/** КПП без ИНН в том же блоке (остаётся отдельным магнитом). */
export const KPP_RU_STANDALONE_SOURCE = String.raw`(?:^|(?<![\p{L}\p{N}]))(?:КПП|кпп)\s*:?\s*\d{9}(?!\d)`;
